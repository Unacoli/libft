#include "libft.h"

int	ft_atoi(char *str)
{
	int	n;
	int	s;
	int i;

	n = 0;
	i = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\r' || str[i] == '\t'
			|| str[i] == '\v' || str[i] == '\f')
		i++;
	s = 1;
	while (str[i] < '0' || s > '9')
	{
		if (str[i] == '-')
			s = -s;
		if (str[i] != '-' && str[i] != '+')
			return (0);
		i++;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		n = n * 10 + (str[i] - '0');
		i++;
	}
	return (n * s);
}
